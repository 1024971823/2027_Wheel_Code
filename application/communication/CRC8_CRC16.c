/**
 * @brief  计算CRC8校验值
 */
uint8_t get_CRC8_check_sum(const uint8_t *pchMessage, uint32_t dwLength, uint8_t ucCRC8)
{
    if (pchMessage == (void *)0) return 0xFF;

    while (dwLength--) {
        ucCRC8 = CRC8_TAB[ucCRC8 ^ (*pchMessage++)];
    }
    return ucCRC8;
}

/**
 * @brief  验证CRC8校验
 * @retval 1=通过, 0=失败
 */
uint32_t verify_CRC8_check_sum(const uint8_t *pchMessage, uint32_t dwLength)
{
    if (pchMessage == (void *)0 || dwLength <= 2) return 0;

    uint8_t expected = get_CRC8_check_sum(pchMessage, dwLength - 1, 0xFF);
    return (expected == pchMessage[dwLength - 1]);
}

/**
 * @brief  追加CRC8校验到消息末尾
 */
void append_CRC8_check_sum(uint8_t *pchMessage, uint32_t dwLength)
{
    if (pchMessage == (void *)0 || dwLength <= 2) return;

    pchMessage[dwLength - 1] = get_CRC8_check_sum(pchMessage, dwLength - 1, 0xFF);
}

/**
 * @brief  计算CRC16校验值
 */
uint16_t get_CRC16_check_sum(const uint8_t *pchMessage, uint32_t dwLength, uint16_t wCRC)
{
    if (pchMessage == (void *)0) return 0xFFFF;

    while (dwLength--) {
        uint8_t data = *pchMessage++;
        wCRC = (wCRC >> 8) ^ CRC16_TAB[(wCRC ^ (uint16_t)data) & 0x00FF];
    }
    return wCRC;
}

/**
 * @brief  验证CRC16校验
 * @retval 1=通过, 0=失败
 */
uint32_t verify_CRC16_check_sum(const uint8_t *pchMessage, uint32_t dwLength)
{
    if (pchMessage == (void *)0 || dwLength <= 2) return 0;

    uint16_t expected = get_CRC16_check_sum(pchMessage, dwLength - 2, 0xFFFF);
    uint16_t actual = ((uint16_t)pchMessage[dwLength - 1] << 8) | pchMessage[dwLength - 2];
    return (expected == actual);
}

/**
 * @brief  追加CRC16校验到消息末尾
 */
void append_CRC16_check_sum(uint8_t *pchMessage, uint32_t dwLength)
{
    if (pchMessage == (void *)0 || dwLength <= 2) return;

    uint16_t crc = get_CRC16_check_sum(pchMessage, dwLength - 2, 0xFFFF);
    pchMessage[dwLength - 2] = (uint8_t)(crc & 0x00FF);
    pchMessage[dwLength - 1] = (uint8_t)((crc >> 8) & 0x00FF);
}
